package FUNCTION;
class FRUITS
{
    static void winter()
    {
        System.out.println("WINTER SEASON");
        System.out.println("GRAPES");
    }
    static void summer()
    {
        System.out.println("SUMMER SEASON");
        System.out.println("MANGO");
    }
    static void main()
    {
       FRUITS sa1= new FRUITS();
       FRUITS sa2= new FRUITS();
       sa1.winter();
       sa2.summer();
    }
}